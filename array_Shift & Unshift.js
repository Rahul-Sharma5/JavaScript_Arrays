// ! JavaScript Array Shift & Unshift

// ! Shift Remove first index element from array //

var a = ["Sanjay", "Aman", "Rohit", "Rohan"]

a.shift();

console.log(a);


// ! UnShift Added first index element from array //

var b = ["Sanjay", "Aman", "Rohit", "Rohan"]

b.unshift("Rahul");

console.log(b);
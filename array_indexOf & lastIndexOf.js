// ! JavaScript Array indexOf & lastIndexOf  //

// ! indexOf array to find index value from an exiting array //


var a = ["Sanjay", "Aman", "Rohit", "Aman", "Amit"];

var b = a.indexOf("Aman", 2); // indexOf("Search item", Start)

console.log(b);



// ! lastIndexOf array to find last index value from an exiting array //

var c = a.lastIndexOf("Aman", 5); // lastIndexOf("Search item", Start)

console.log(c);
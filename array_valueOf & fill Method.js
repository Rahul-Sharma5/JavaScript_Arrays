// !   JavaScript Array toString, valueOf & fill Methods



//! toString() Methods
// The toString() methods converts an array into String and returns the result.

var a = ["Ram", "Shyam", "Rohan"];

var b = a.toString();

console.log(b);



//! valueOf() Methods
// The valueOf() method returns the array

var c = ["Rohit", "Shiv", "Ram"];

var d = c.valueOf();

console.log(d);




//! fill() Methods
// The fill() method fills all the elements in an array with a static value.
var e = ["Sanjay", "Aman", "Rohit", "Rohan", "Amit"];

var f = e.fill("Ram");

console.log(f);
